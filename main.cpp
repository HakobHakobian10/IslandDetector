class Point
{
public:
    int I = 0;
    int J = 0;

    Point(int i,int j) 
	: I(i)
	, J(j)
    {
    }

};

Pixel island;
std::list<Point> front;

void SetisVisited(bool a)
{
    for(int i = 0;i < size;++i)
    {
	for(int j = 0;j < size;++j)
	{
	    island.isVisited[i][j] = a;
	}
    }
}
void ProcessNeighbors(std::list<Point>& nextFront, Point& current, int count)
{
    for (int ii = current.I - 1; ii <= current.I + 1; ++ii)
    {
        for (int jj = current.J - 1; jj <= current.J + 1; ++jj)
	{
            if(ii != current.I || jj != current.J)
	    if(ii >= 0 && jj >= 0 && ii < size && jj < size && island.matrix[current.I][current.J] == island.matrix[ii][jj] &&!island.isVisited[ii][jj])
	    {    
                nextFront.push_back(Point(ii, jj));
                island.isVisited[ii][jj] = true;
                island.includingMatrix[ii][jj] = count;
            }
        }
    }
}
void SetNumber()
{
    SetisVisited(false);
    int count = 0;

    for(int i = 0;i < size; ++i)
    {
	for(int j = 0;j < size; ++j)
	{
	    if(!island.isVisited[i][j])
	    {
		++count;
		island.isVisited[i][j] = true;
		island.includingMatrix[i][j] = count;
		front.push_back(Point(i, j));

		while(!front.empty())
		{
		    std::list<Point> nextFront;
		    for(std::list<Point>::iterator iter = front.begin();iter != front.end();++iter)
		    {
			ProcessNeighbors(nextFront,*iter,count);
		    }
		    front = nextFront;
		}
	    }
	}
    }
}

void PrintMatrix(int matrix[size][size])
{
    for(int i = 0;i < size;++i)
    {
	for(int j = 0;j < size;++j)
	{
	    std::cout << matrix[i][j] << " ";
	}
	std::cout << std::endl;
    }
}

